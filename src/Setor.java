public class Setor {
  final private String nome;
  final private String descricao;

  public String getNome() {
    return nome;
  }


  public String getDescricao() {
    return descricao;
  }


  public Setor(String nome, String descricao) {
    this.nome = nome;
    this.descricao = descricao;
  }

}
